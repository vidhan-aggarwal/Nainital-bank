window.NB_I18N.apply = function (code) {
  const dict = window.NB_I18N[code] || window.NB_I18N.en;
  document.documentElement.lang = code === 'hi' ? 'hi' : 'en';
  document.body.setAttribute('data-nb-lang', code);
  localStorage.setItem('nainital-lang', code);

  document.querySelectorAll('[data-nb-i18n]').forEach((el) => {
    const key = el.getAttribute('data-nb-i18n');
    if (dict[key] === undefined) return;
    const text = dict[key];
    if (el.hasAttribute('data-nb-i18n-br') || (text.includes('\n') && el.tagName !== 'INPUT')) {
      el.innerHTML = text.split('\n').join('<br>');
    } else {
      el.textContent = text;
    }
  });

  document.querySelectorAll('[data-nb-i18n-placeholder]').forEach((el) => {
    const key = el.getAttribute('data-nb-i18n-placeholder');
    if (dict[key] !== undefined) el.placeholder = dict[key];
  });

  document.querySelectorAll('[data-nb-i18n-aria]').forEach((el) => {
    const key = el.getAttribute('data-nb-i18n-aria');
    if (dict[key] !== undefined) el.setAttribute('aria-label', dict[key]);
  });

  document.querySelectorAll('[data-nb-i18n-alt]').forEach((el) => {
    const key = el.getAttribute('data-nb-i18n-alt');
    if (dict[key] !== undefined) el.alt = dict[key];
  });

  if (dict['page.title']) document.title = dict['page.title'];
  const meta = document.querySelector('meta[name="description"]');
  if (meta && dict['page.description']) meta.setAttribute('content', dict['page.description']);

  const skip = document.querySelector('.skip-link');
  if (skip && dict['skip.main']) skip.textContent = dict['skip.main'];
};

(function () {
  function bootLang() {
    const saved = localStorage.getItem('nainital-lang');
    if (saved && saved !== 'en') window.NB_I18N.apply(saved);
  }
  if (document.readyState === 'loading') {
    document.addEventListener('DOMContentLoaded', bootLang);
  } else {
    bootLang();
  }
})();
